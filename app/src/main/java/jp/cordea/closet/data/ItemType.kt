package jp.cordea.closet.data

enum class ItemType(val attributes: Set<ItemAttribute>) {
    TOPS(
        setOf(
            ItemAttribute.BUST,
            ItemAttribute.LENGTH,
            ItemAttribute.SLEEVE_LENGTH,
            ItemAttribute.SHOULDER_WIDTH,
            ItemAttribute.NECK_SIZE
        )
    ),
    OUTERWEAR(
        setOf(
            ItemAttribute.BUST,
            ItemAttribute.LENGTH,
            ItemAttribute.SLEEVE_LENGTH,
            ItemAttribute.SHOULDER_WIDTH,
            ItemAttribute.NECK_SIZE
        )
    ),
    PANTS(
        setOf(
            ItemAttribute.LENGTH,
            ItemAttribute.WAIST,
            ItemAttribute.HIP,
            ItemAttribute.INSEAM,
            ItemAttribute.RISE,
            ItemAttribute.LEG_OPENING,
            ItemAttribute.KNEE,
            ItemAttribute.THIGH
        )
    ),
    SKIRTS(
        setOf(
            ItemAttribute.LENGTH,
            ItemAttribute.WAIST,
            ItemAttribute.HIP
        )
    ),
    DRESSES(
        setOf(
            ItemAttribute.BUST,
            ItemAttribute.LENGTH,
            ItemAttribute.WAIST,
            ItemAttribute.HIP,
            ItemAttribute.SLEEVE_LENGTH,
            ItemAttribute.SHOULDER_WIDTH,
            ItemAttribute.NECK_SIZE
        )
    ),
    SHOES(
        setOf(ItemAttribute.WIDTH)
    ),
    BAGS(
        setOf(
            ItemAttribute.HEIGHT,
            ItemAttribute.WIDTH,
            ItemAttribute.DEPTH
        )
    ),
    HATS(
        setOf(
            ItemAttribute.HEAD_CIRCUMFERENCE
        )
    ),
    RINGS(setOf()),
    OTHERS(ItemAttribute.entries.toSet())
}
